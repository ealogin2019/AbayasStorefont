  try {
    // Clear existing data in correct order (foreign key constraints)
    console.log("Clearing existing data...");
    await prisma.orderItem.deleteMany({});
    await prisma.order.deleteMany({});
    await prisma.cartItem.deleteMany({});
    const deleteResult = await prisma.product.deleteMany({});
    console.log(`âœ… Cleared ${deleteResult.count} existing products`);

    // Insert new products
    let successCount = 0;
    for (const product of abayaProducts) {
      try {
        await prisma.product.create({
          data: {
            ...product,
            currency: "AED",
            inStock: true,
            quantity: Math.floor(Math.random() * 50) + 10, // Random quantity between 10-60
          },
        });
        successCount++;
        console.log(`âœ“ Added: ${product.name}`);
      } catch (error) {
        console.error(`âœ— Failed to add ${product.name}:`, error);
      }
    }

    console.log(`\nâœ… Successfully seeded ${successCount} abaya products!`);
    console.log(`ðŸ’° Price range: AED ${Math.min(...abayaProducts.map(p => p.price))} - AED ${Math.max(...abayaProducts.map(p => p.price))}`);
    console.log(`ðŸŽ¨ Total unique styles: ${successCount}`);
  } catch (error) {
    console.error("âŒ Error seeding database:", error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
